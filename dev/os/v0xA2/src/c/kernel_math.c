int64_t pow( int32_t _bs, int32_t _pow ) {

    // -----------------------------------------------------------
    // FIXME: Negative power (not implemented)

    int32_t i = 0;

    uint64_t res = _bs;

    while ( i++ < _pow-1 ) {

        res *= _bs;

    }

    return res;
    // -----------------------------------------------------------

}

